n = 12345678
# Преобразуем число в строку, чтобы можно было итерировать по каждой цифре
digits = str(n)
# Инициализируем переменную для хранения суммы
total = 0
# Итерируем по каждой цифре и добавляем её к сумме
for digit in digits:
    total += int(digit)
# Выводим результат
print("Сумма цифр числа:", total)
